                        physics</a>, I've used the <b>functional renormalization group</b> to <a
                        href="http://journals.aps.org/prb/abstract/10.1103/PhysRevB.92.121409">study the effect of
                        interactions in graphene</a>, and I've used a (real) <b>quantum computer</b> to run <a
                        href="https://journals.aps.org/prresearch/abstract/10.1103/PhysRevResearch.4.033160">parallel
                        quantum chemistry simulations</a>.

                    <br>
                    <br>
                    I actively contribute to many free and <b>open source software projects</b>, mostly focused on
                    scientific computing and <a href="https://julialang.org/">Julia</a>. In particular, I act as a
                    co-organizer of <a href="https://juliacon.org/">JuliaCon</a> and, until recently, was
                    editor-in-chief of the <a href="https://proceedings.juliacon.org/">Julia Proceedings</a>.
                    <br>
                    <br>
                    As a <b>freelancer</b>, I offer consulting services and deliver <a href="#workshops">workshops</a>
                    at public and private institutions. (Feel free to <a href="#contact">reach out</a>.)
                    <br>
