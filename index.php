                    the (parallel-)performance of software.

                    <br>
                    <br>
                    In my research as a <b>scientist</b>, I develop and use a variety of novel methods and technologies
                    to gain insights into quantum systems. In particular, I've developed a <b>quantum monte carlo</b>
                    code (in <a href="https://julialang.org/">Julia</a>) to <a
                        href="https://link.aps.org/doi/10.1103/PhysRevResearch.2.023008">study metallic quantum
